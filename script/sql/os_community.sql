-- 项目信息表

CREATE TABLE `os_project` (
  `project_id` BIGINT NOT NULL AUTO_INCREMENT COMMENT '项目ID',
  `project_name` VARCHAR(100) NOT NULL COMMENT '项目名称',
  `project_code` VARCHAR(50) DEFAULT NULL COMMENT '项目代号',
  `description` TEXT COMMENT '项目描述',
  `repository_url` VARCHAR(255) DEFAULT NULL COMMENT '代码仓库URL',
  `website_url` VARCHAR(255) DEFAULT NULL COMMENT '项目网站',
  `logo_url` VARCHAR(255) DEFAULT NULL COMMENT '项目Logo',
  `status` CHAR(1) DEFAULT '0' COMMENT '状态（0草稿 1审核中 2已通过 3已拒绝 4已暂停）',
  `create_dept` BIGINT DEFAULT NULL COMMENT '创建部门',
  `create_by` BIGINT DEFAULT NULL COMMENT '创建者',
  `create_time` DATETIME DEFAULT NULL COMMENT '创建时间',
  `update_by` BIGINT DEFAULT NULL COMMENT '更新者',
  `update_time` DATETIME DEFAULT NULL COMMENT '更新时间',
  `remark` VARCHAR(500) DEFAULT NULL COMMENT '备注',
  `tech_stack` VARCHAR(500) DEFAULT NULL COMMENT '技术栈',
  `programming_language` VARCHAR(200) DEFAULT NULL COMMENT '编程语言',
  `last_commit_time` DATETIME DEFAULT NULL COMMENT '最后提交时间',
  `star_count` INT DEFAULT 0 COMMENT 'Star数',
  `fork_count` INT DEFAULT 0 COMMENT 'Fork数',
  `core_contributors` TEXT COMMENT '核心贡献者',
  `contact_info` VARCHAR(500) DEFAULT NULL COMMENT '联系方式',
  `version_info` VARCHAR(100) DEFAULT NULL COMMENT '版本信息',
  `issues_count` INT DEFAULT 0 COMMENT 'Issues数',
  `pr_count` INT DEFAULT 0 COMMENT 'PR数',
  `readme_url` VARCHAR(500) DEFAULT NULL COMMENT 'README链接',
  `wiki_url` VARCHAR(500) DEFAULT NULL COMMENT 'Wiki链接',
  `api_doc_url` VARCHAR(500) DEFAULT NULL COMMENT 'API文档链接',
  `user_id` BIGINT DEFAULT NULL COMMENT '项目负责人ID',
  `application_type` VARCHAR(50) DEFAULT 'personal' COMMENT '申请类型 (personal-个人开源项目, community-社区项目成为顶级项目)',
  `application_status` VARCHAR(50) DEFAULT 'draft' COMMENT '申请状态 (draft-草稿, pending-待审核, approved-审核通过, rejected-审核拒绝)',
  `license` VARCHAR(100) DEFAULT NULL COMMENT '开源协议',
  `application_reason` TEXT COMMENT '申请理由/价值说明',
  `contribution` TEXT COMMENT '社区贡献',
  `current_status` TEXT COMMENT '当前项目状态',
  `upgrade_reason` TEXT COMMENT '升级理由/价值说明',
  `community_impact` TEXT COMMENT '社区影响力',
  `contact_email` VARCHAR(255) DEFAULT NULL COMMENT '申请人邮箱',
  `contact_phone` VARCHAR(50) DEFAULT NULL COMMENT '联系电话',
  `remarks` VARCHAR(255) DEFAULT NULL COMMENT '备注',
  `watch_count` INT DEFAULT 0 COMMENT 'Watch数（关注数）',
  `project_size` VARCHAR(50) DEFAULT '0' COMMENT '项目大小（KB）',
  `last_sync_time` DATETIME DEFAULT NULL COMMENT '最后同步时间',
  PRIMARY KEY (`project_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_520_ci COMMENT='开源项目表';

-- 项目审核表

CREATE TABLE `os_project_audit` (
  `audit_id` BIGINT NOT NULL AUTO_INCREMENT COMMENT '审核ID',
  `project_id` BIGINT NOT NULL COMMENT '项目ID',
  `audit_status` CHAR(1) NOT NULL COMMENT '审核状态（0待审核 1通过 2拒绝 3退回修改）',
  `audit_user` BIGINT DEFAULT NULL COMMENT '审核人',
  `audit_time` DATETIME DEFAULT NULL COMMENT '审核时间',
  `audit_opinion` VARCHAR(500) DEFAULT NULL COMMENT '审核意见',
  `create_time` DATETIME DEFAULT NULL COMMENT '创建时间',
  `update_time` DATETIME DEFAULT NULL COMMENT '更新时间',
  `create_dept` BIGINT DEFAULT NULL COMMENT '创建部门',
  `update_by` BIGINT DEFAULT NULL COMMENT '更新者',
  `create_by` BIGINT DEFAULT NULL COMMENT '创建者',
  `join_project_list` TINYINT(1) DEFAULT 0 COMMENT '是否加入项目列表',
  PRIMARY KEY (`audit_id`),
  INDEX `idx_project_id` (`project_id`),
  INDEX `idx_audit_status` (`audit_status`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_520_ci COMMENT='项目审核表';

-- 贡献信息表
CREATE TABLE `os_contribution` (
  `contribution_id` BIGINT NOT NULL AUTO_INCREMENT COMMENT '贡献ID',
  `user_id` BIGINT NOT NULL COMMENT '用户ID',
  `project_id` BIGINT NOT NULL COMMENT '项目ID',
  `contribution_type` CHAR(1) DEFAULT '0' COMMENT '贡献类型（0代码提交 1问题修复 2文档贡献 3回答问题 4其他）',
  `content` VARCHAR(500) DEFAULT NULL COMMENT '贡献内容',
  `url` VARCHAR(255) DEFAULT NULL COMMENT '相关链接',
  `contribution_time` DATETIME DEFAULT NULL COMMENT '贡献时间',
  `points` INT DEFAULT 0 COMMENT '贡献点数',
  `create_time` DATETIME DEFAULT NULL COMMENT '创建时间',
  `update_time` DATETIME DEFAULT NULL COMMENT '更新时间',
  `create_dept` BIGINT DEFAULT NULL COMMENT '创建部门',
  `create_by` BIGINT DEFAULT NULL COMMENT '创建人',
  `update_by` BIGINT DEFAULT NULL COMMENT '更新人',
  PRIMARY KEY (`contribution_id`),
  INDEX `idx_user_id` (`user_id`),
  INDEX `idx_project_id` (`project_id`),
  INDEX `idx_contribution_type` (`contribution_type`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_520_ci COMMENT='项目贡献表';


